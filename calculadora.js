function soma(num1,num2){
    let resposta = num1 + num2
    console.log("o resultado da soma é: "+ resposta)
    return resposta
}

function subtracao(num1,num2){
    let resposta = num1 - num2
    console.log("o resultado da subtração é: "+ resposta)
    return resposta
}

function multiplicacao(num1,num2){
    let resposta = num1 * num2
    console.log("o resultado da divisão é: "+ resposta)
    return resposta
}

function divisao(num1,num2){
    let resposta = num1 / num2
    console.log("o resultado da multiplicação é: "+ resposta)
    return resposta
}

console.log(soma(50,10))
console.log(subtracao(50,10))
console.log(multiplicacao(50,10))
console.log(divisao(50,10))
console.log(divisao(20,0))
